func TestDiagnosticsCapabilityQueryProducesExactSignedFlags(t *testing.T) {
	appPrivate := ed25519.NewKeyFromSeed(bytes.Repeat([]byte{0x71}, ed25519.SeedSize))
	helperPrivate := ed25519.NewKeyFromSeed(bytes.Repeat([]byte{0x72}, ed25519.SeedSize))
	context := diagnosticsUploadVerificationContext{
		appPublicKey:    appPrivate.Public().(ed25519.PublicKey),
		helperPublicKey: helperPrivate.Public().(ed25519.PublicKey),
	}
	now := time.Unix(1_800_000_000, 0)
	binding := diagnosticsUploadBinding{
		homeserverBinding: bytes.Repeat([]byte{0x73}, 32),
		folderBinding:     bytes.Repeat([]byte{0x74}, 32),
		appPublicKey:      context.appPublicKey,
		helperPrivateKey:  helperPrivate,
		appEpoch:          3,
		helperEpoch:       4,
	}
	appKeyID := diagnosticsKeyID(context.appPublicKey)
	helperKeyID := diagnosticsKeyID(context.helperPublicKey)
	queryBody := diagnosticsCBORMapValue(
		diagnosticsCBORMapField(1, diagnosticsCBORTextValue(diagnosticsRoundtripCapabilityID)),
		diagnosticsCBORMapField(2, diagnosticsCBORUint(1)),
		diagnosticsCBORMapField(3, diagnosticsCBORUint(1)),
		diagnosticsCBORMapField(4, diagnosticsCBORUint(diagnosticsCapabilityQuery)),
		diagnosticsCBORMapField(5, diagnosticsCBORBstr(binding.homeserverBinding)),
		diagnosticsCBORMapField(6, diagnosticsCBORBstr(binding.folderBinding)),
		diagnosticsCBORMapField(7, diagnosticsCBORBstr(appKeyID[:])),
		diagnosticsCBORMapField(8, diagnosticsCBORBstr(helperKeyID[:])),
		diagnosticsCBORMapField(9, diagnosticsCBORUint(binding.appEpoch)),
		diagnosticsCBORMapField(10, diagnosticsCBORUint(binding.helperEpoch)),
		diagnosticsCBORMapField(12, diagnosticsCBORUint(uint64(now.Unix()))),
		diagnosticsCBORMapField(13, diagnosticsCBORUint(uint64(now.Add(2*time.Minute).Unix()))),
		diagnosticsCBORMapField(30, diagnosticsCBORBstr(bytes.Repeat([]byte{0x75}, 32))),
	)
	queryBytes, err := signDiagnosticsCapabilityMessage(queryBody, diagnosticsCapabilityQuery, appPrivate, context)
	if err != nil {
		t.Fatal(err)
	}
	query, err := decodeDiagnosticsCapabilityMessage(queryBytes, context)
	if err != nil {
		t.Fatal(err)
	}
	responseBytes, err := buildDiagnosticsCapabilityResponse(query, binding, now.Add(time.Second))
	if err != nil {
		t.Fatal(err)
	}
	response, err := decodeDiagnosticsCapabilityMessage(responseBytes, context)
	if err != nil {
		t.Fatal(err)
	}
	flags, _ := diagnosticsUploadUintField(response.value, 27)
	prior, _ := diagnosticsUploadBytesField(response.value, 31, 32)
	if flags != diagnosticsRoundtripRequiredBits || !bytes.Equal(prior, query.digest[:]) {
		t.Fatalf("capability response flags/prior = %x/%x", flags, prior)
	}
	if !bytes.Equal(queryBytes, diagnosticsCapabilityGoldenQueryBytes) || !bytes.Equal(responseBytes, diagnosticsCapabilityGoldenResponseBytes) {
		t.Fatalf("capability golden bytes changed")
	}

	tampered := append([]byte(nil), responseBytes...)
	tampered[len(tampered)-1] ^= 1
	if _, err := decodeDiagnosticsCapabilityMessage(tampered, context); err == nil {
		t.Fatal("tampered capability response was accepted")
	}
}

func FuzzDiagnosticsCapabilityDecoder(f *testing.F) {
	f.Add(diagnosticsCapabilityGoldenQueryBytes)
	f.Add(diagnosticsCapabilityGoldenResponseBytes)
	f.Add([]byte{})

	appPrivate := ed25519.NewKeyFromSeed(bytes.Repeat([]byte{0x71}, ed25519.SeedSize))
	helperPrivate := ed25519.NewKeyFromSeed(bytes.Repeat([]byte{0x72}, ed25519.SeedSize))
	context := diagnosticsUploadVerificationContext{
		appPublicKey:    appPrivate.Public().(ed25519.PublicKey),
		helperPublicKey: helperPrivate.Public().(ed25519.PublicKey),
	}
	f.Fuzz(func(_ *testing.T, body []byte) {
		_, _ = decodeDiagnosticsCapabilityMessage(body, context)
	})
}

func TestDiagnosticsCapabilityRejectsStaleAndWrongTuple(t *testing.T) {
	appPrivate := ed25519.NewKeyFromSeed(bytes.Repeat([]byte{0x81}, ed25519.SeedSize))
	helperPrivate := ed25519.NewKeyFromSeed(bytes.Repeat([]byte{0x82}, ed25519.SeedSize))
	context := diagnosticsUploadVerificationContext{appPublicKey: appPrivate.Public().(ed25519.PublicKey), helperPublicKey: helperPrivate.Public().(ed25519.PublicKey)}
	appKeyID := diagnosticsKeyID(context.appPublicKey)
	helperKeyID := diagnosticsKeyID(context.helperPublicKey)
	issued := uint64(1_800_000_000)
	value := diagnosticsCBORMapValue(
		diagnosticsCBORMapField(1, diagnosticsCBORTextValue(diagnosticsRoundtripCapabilityID)),
		diagnosticsCBORMapField(2, diagnosticsCBORUint(1)), diagnosticsCBORMapField(3, diagnosticsCBORUint(1)),
		diagnosticsCBORMapField(4, diagnosticsCBORUint(diagnosticsCapabilityQuery)),
		diagnosticsCBORMapField(5, diagnosticsCBORBstr(bytes.Repeat([]byte{0x83}, 32))),
		diagnosticsCBORMapField(6, diagnosticsCBORBstr(bytes.Repeat([]byte{0x84}, 32))),
		diagnosticsCBORMapField(7, diagnosticsCBORBstr(appKeyID[:])), diagnosticsCBORMapField(8, diagnosticsCBORBstr(helperKeyID[:])),
		diagnosticsCBORMapField(9, diagnosticsCBORUint(1)), diagnosticsCBORMapField(10, diagnosticsCBORUint(1)),
		diagnosticsCBORMapField(12, diagnosticsCBORUint(issued)), diagnosticsCBORMapField(13, diagnosticsCBORUint(issued+120)),
		diagnosticsCBORMapField(30, diagnosticsCBORBstr(bytes.Repeat([]byte{0x85}, 32))),
	)
	encoded, err := signDiagnosticsCapabilityMessage(value, diagnosticsCapabilityQuery, appPrivate, context)
	if err != nil {
		t.Fatal(err)
	}
	message, _ := decodeDiagnosticsCapabilityMessage(encoded, context)
	if err := validateDiagnosticsCapabilityClock(message, time.Unix(int64(issued+300), 0)); err == nil {
		t.Fatal("stale capability query was accepted")
	}
	wrongTuple := diagnosticsUploadBinding{
		homeserverBinding: bytes.Repeat([]byte{0x83}, 32),
		folderBinding:     bytes.Repeat([]byte{0xaa}, 32),
		appEpoch:          1,
		helperEpoch:       1,
	}
	if diagnosticsCapabilityMatchesBinding(message, wrongTuple) {
		t.Fatal("capability query for a different folder tuple was accepted")
	}
}
